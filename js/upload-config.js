/* ==========================================================================
   تنظیمات هاست آپلود اختصاصی (مخصوص مدیر سایت)

   با فعال‌کردن این تنظیمات، فایل‌های صوتی/ویدیویی کاربران به‌جای سرویس‌های
   عمومی، روی هاست خودتان آپلود می‌شوند (برای همهٔ بازدیدکنندگان).

   راه ساده: در خود سایت، فرم «صدا» یا «ویدیو» → «آپلود از دستگاه» →
   «تنظیمات هاست آپلود (مدیر)» را باز کنید؛ بعد از واردکردن و تست موفق،
   دکمهٔ «کد پیکربندی» همین محتوا را آماده می‌دهد تا اینجا جای‌گذاری کنید.

   راهنمای فیلدها:
   - enabled          : true شود تا هاست اختصاصی فعال گردد
   - name             : نام نمایشی سرویس (در پیام‌ها نشان داده می‌شود)
   - endpoint         : آدرس API آپلود — «{filename}» با نام فایل جایگزین می‌شود
   - method           : "POST" (فرم multipart) یا "PUT" (بدنهٔ خام فایل)
   - fieldName        : نام فیلد فایل در حالت POST (معمولاً "file")
   - headers          : هدرهای اضافه مثل توکن؛ نمونه: { "Authorization": "Bearer XXX" }
   - responseUrlPath  : محل لینک در پاسخ سرور:
                          "text"      یعنی کل پاسخ، خودِ لینک است
                          "data.url"  یعنی مسیر داخل JSON پاسخ
   - urlPrefix        : اگر پاسخ فقط شناسه است، پیشوند ساخت لینک نهایی
   - useDefaultFallbacks : اگر true باشد، درصورت خطای هاست شما،
                           سرویس‌های عمومی پیش‌فرض امتحان می‌شوند

   نکته: سرور شما باید CORS را برای دامنهٔ این سایت مجاز کند
   (Access-Control-Allow-Origin) وگرنه آپلود از مرورگر ممکن نیست.
   ========================================================================== */

